    public void updateList() {
        model.setRowCount(0);
        for (BoardModel b : BoardController.getAllBoardById(this.id_user)) {
            model.addRow(new Object[]{b.getId_board(), b.getTitle_board(), b.getDescription_board()});
        }
        this.boardList.setModel(model);
    }

